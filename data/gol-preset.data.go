package data

type PresetType struct {
	Name   string
	Matrix [][]uint8
}

func GetPreset(gameType string, presetString string) PresetType {
	if gameType == "Game of Life" {
		//return GoLPresets[preset]

		for _, preset := range GoLPresets {
			if preset.Name == presetString {
				return preset
			}
		}
	}

	return PresetType{}
}

func GetAllGoLPresets() []PresetType {
	var presets []PresetType

	for _, preset := range GoLPresets {
		presets = append(presets, preset)
	}

	return presets
}

